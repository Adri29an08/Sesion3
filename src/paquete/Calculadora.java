package paquete;

public class Calculadora {

	public static Integer suma(int i, int j) {
		return i+j;
	}

	public static Integer resta(int i, int j) {
		return i-j;
	}

	public static Integer multiplicar(int i, int j) {
		return i*j;
	}

	public static Integer dividir(int i, int j) {
		return i/j;
	}

}
